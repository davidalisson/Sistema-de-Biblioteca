package br.com.sisbiblioteca.cadastrousuarios;

/**
 *
 * @author Murilo Alves
 */
public class usuarioBean {

    private Integer idfuncionario;
    private String nomefuncionario;
    private String endfuncionario;
    private String bairro;
    private String celular;
    private String cpf;
    private String login;
    private String senha;

    /**
     * @return the idfuncionario
     */
    public Integer getIdfuncionario() {
        return idfuncionario;
    }

    /**
     * @param idfuncionario the idfuncionario to set
     */
    public void setIdfuncionario(Integer idfuncionario) {
        this.idfuncionario = idfuncionario;
    }

    /**
     * @return the nomefuncionario
     */
    public String getNomefuncionario() {
        return nomefuncionario;
    }

    /**
     * @param nomefuncionario the nomefuncionario to set
     */
    public void setNomefuncionario(String nomefuncionario) {
        this.nomefuncionario = nomefuncionario;
    }

    /**
     * @return the endfuncionario
     */
    public String getEndfuncionario() {
        return endfuncionario;
    }

    /**
     * @param endfuncionario the endfuncionario to set
     */
    public void setEndfuncionario(String endfuncionario) {
        this.endfuncionario = endfuncionario;
    }

    /**
     * @return the bairro
     */
    public String getBairro() {
        return bairro;
    }

    /**
     * @param bairro the bairro to set
     */
    public void setBairro(String bairro) {
        this.bairro = bairro;
    }

    /**
     * @return the celular
     */
    public String getCelular() {
        return celular;
    }

    /**
     * @param celular the celular to set
     */
    public void setCelular(String celular) {
        this.celular = celular;
    }

    /**
     * @return the cpf
     */
    public String getCpf() {
        return cpf;
    }

    /**
     * @param cpf the cpf to set
     */
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    /**
     * @return the login
     */
    public String getLogin() {
        return login;
    }

    /**
     * @param login the login to set
     */
    public void setLogin(String login) {
        this.login = login;
    }

    /**
     * @return the senha
     */
    public String getSenha() {
        return senha;
    }

    /**
     * @param senha the senha to set
     */
    public void setSenha(String senha) {
        this.senha = senha;
    }
}
